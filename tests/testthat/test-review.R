describe("build_checklist", {
  it("includes base checklist for any files", {
    result <- build_checklist("README.md", "jinx")
    expect_true(grepl("Review Checklist", result))
    expect_true(grepl("sensitive information", result))
  })

  it("adds JSON section for .json files", {
    result <- build_checklist("data/entry.json", "jinx")
    expect_true(grepl("JSON files", result))
    expect_true(grepl("well-formatted", result))
  })

  it("adds R code section for .R files", {
    result <- build_checklist("R/foo.R", "jinx")
    expect_true(grepl("R code", result))
    expect_true(grepl("roxygen2", result))
  })

  it("adds workflow section for workflow files", {
    result <- build_checklist(".github/workflows/ci.yml", "jinx")
    expect_true(grepl("GitHub Actions", result))
    expect_true(grepl("pinned versions", result))
  })

  it("adds content section for markdown files", {
    result <- build_checklist("content/blog/post.md", "jinx")
    expect_true(grepl("Content", result))
    expect_true(grepl("alt text", result))
  })

  it("adds data section with directory-specific items", {
    result <- build_checklist("data/entry.json", "directory")
    expect_true(grepl("Data", result))
    expect_true(grepl("Social media handles", result))
  })

  it("adds data section without directory items for other repos", {
    result <- build_checklist("data/entry.json", "jinx")
    expect_true(grepl("Data", result))
    expect_false(grepl("Social media handles", result))
  })

  it("handles multiple file types", {
    files <- c("R/foo.R", "data/bar.json", ".github/workflows/ci.yml")
    result <- build_checklist(files, "jinx")
    expect_true(grepl("R code", result))
    expect_true(grepl("JSON files", result))
    expect_true(grepl("GitHub Actions", result))
  })

  it("includes jinx signature", {
    result <- build_checklist("foo.txt", "jinx")
    expect_true(grepl("Generated by jinx", result))
  })
})

describe("check_naming_conventions", {
  it("accepts valid lowercase json filenames", {
    result <- check_naming_conventions("data/buenos-aires.json", "directory")
    expect_true(result$valid[1])
  })

  it("rejects uppercase in json filenames", {
    result <- check_naming_conventions("data/BuenosAires.json", "directory")
    expect_false(result$valid[1])
    expect_true(grepl("lowercase", result$reason[1]))
  })

  it("rejects special characters in json filenames", {
    result <- check_naming_conventions("data/buenos aires.json", "directory")
    expect_false(result$valid[1])
    expect_true(grepl("invalid characters", result$reason[1]))
  })

  it("rejects leading hyphen", {
    result <- check_naming_conventions("data/-buenos.json", "directory")
    expect_false(result$valid[1])
    expect_true(grepl("hyphen", result$reason[1]))
  })

  it("rejects trailing hyphen", {
    result <- check_naming_conventions("data/buenos-.json", "directory")
    expect_false(result$valid[1])
    expect_true(grepl("hyphen", result$reason[1]))
  })

  it("skips non-json files", {
    result <- check_naming_conventions("R/BadName.R", "directory")
    expect_true(result$valid[1])
  })

  it("only checks data/blogs/contact paths", {
    result <- check_naming_conventions("src/BadName.json", "directory")
    expect_true(result$valid[1])
  })

  it("checks blogs path", {
    result <- check_naming_conventions("blogs/Bad Name.json", "directory")
    expect_false(result$valid[1])
  })

  it("reports multiple reasons", {
    result <- check_naming_conventions("data/-Bad Name.json", "directory")
    expect_false(result$valid[1])
    expect_true(grepl(";", result$reason[1]))
  })
})

describe("format_naming_violations", {
  it("formats violations as markdown table", {
    violations <- data.frame(
      filename = c("data/Bad.json", "data/no good.json"),
      valid = c(FALSE, FALSE),
      reason = c("must be lowercase", "invalid characters"),
      stringsAsFactors = FALSE
    )
    result <- format_naming_violations(violations)
    expect_true(grepl("Naming Convention", result))
    expect_true(grepl("Bad.json", result))
    expect_true(grepl("no good.json", result))
    expect_true(grepl("Generated by jinx", result))
  })
})
