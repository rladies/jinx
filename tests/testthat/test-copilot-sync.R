describe("copilot_strip_frontmatter", {
  it("removes a leading YAML frontmatter block", {
    text <- paste(
      "---",
      "name: run-brand-check",
      "description: gate",
      "---",
      "",
      "# Brand check",
      "body here",
      sep = "\n"
    )
    out <- copilot_strip_frontmatter(text)
    expect_true(startsWith(out, "# Brand check"))
    expect_false(grepl("name: run-brand-check", out, fixed = TRUE))
  })

  it("leaves text without frontmatter untouched", {
    text <- "# Title\nbody"
    expect_identical(copilot_strip_frontmatter(text), text)
  })

  it("does not strip when the block is unterminated", {
    text <- "---\nname: x\nno closing fence"
    expect_identical(copilot_strip_frontmatter(text), text)
  })
})

describe("copilot_review_globs", {
  config <- list(
    repos = list(
      default = list(brand = "**", blog = "content/blog/**"),
      website = list(brand = "**", blog = "posts/**")
    )
  )

  it("returns the repo's own entry when present", {
    globs <- copilot_review_globs("website", config)
    expect_identical(globs$blog, "posts/**")
  })

  it("falls back to default for unknown repos", {
    globs <- copilot_review_globs("mystery", config)
    expect_identical(globs$blog, "content/blog/**")
  })
})

