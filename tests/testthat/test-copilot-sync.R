describe("copilot_render_instructions", {
  skills <- list(
    brand = "BRAND CHECK",
    blog = "BLOG",
    social = "SOCIAL",
    translation = "TRANSLATION",
    brand_context = "BRAND RULESET"
  )
  globs <- list(
    brand = "**",
    blog = "content/blog/**",
    social = "social/**",
    translation = "i18n/**"
  )
  files <- copilot_render_instructions(skills, globs)

  it("produces the repo contract and four gate files", {
    expect_setequal(
      names(files),
      c(
        ".github/copilot-instructions.md",
        ".github/instructions/rladies-brand.instructions.md",
        ".github/instructions/rladies-blog.instructions.md",
        ".github/instructions/rladies-social.instructions.md",
        ".github/instructions/rladies-translation.instructions.md"
      )
    )
  })

  it("merges the brand ruleset and brand-check into the brand file", {
    brand <- files[[".github/instructions/rladies-brand.instructions.md"]]
    expect_true(grepl("BRAND RULESET", brand, fixed = TRUE))
    expect_true(grepl("BRAND CHECK", brand, fixed = TRUE))
    expect_true(grepl("applyTo: \"**\"", brand, fixed = TRUE))
  })

  it("scopes each surface gate to its globs", {
    blog <- files[[".github/instructions/rladies-blog.instructions.md"]]
    expect_true(grepl("applyTo: \"content/blog/**\"", blog, fixed = TRUE))
    expect_true(grepl("BLOG", blog, fixed = TRUE))
  })

  it("keeps the never-publish contract in the repo-wide file", {
    contract <- files[[".github/copilot-instructions.md"]]
    expect_true(grepl("Never approve, merge", contract, fixed = TRUE))
    expect_true(grepl("RLadies+", contract, fixed = TRUE))
  })
})

