describe("copilot_put_file", {
  it("skips the PUT when content is unchanged", {
    calls <- character()
    local_mocked_bindings(
      gh = function(endpoint, ...) {
        calls[[length(calls) + 1]] <<- endpoint
        if (startsWith(endpoint, "GET")) {
          return(list(
            content = jsonlite::base64_enc(charToRaw("same")),
            sha = "s"
          ))
        }
        list()
      },
      .package = "gh"
    )
    changed <- copilot_put_file("o", "r", "p", "same", "b", "m")
    expect_false(changed)
    expect_false(any(startsWith(calls, "PUT")))
  })

  it("PUTs with the existing sha when content differs", {
    calls <- character()
    local_mocked_bindings(
      gh = function(endpoint, ...) {
        calls[[length(calls) + 1]] <<- endpoint
        if (startsWith(endpoint, "GET")) {
          return(list(
            content = jsonlite::base64_enc(charToRaw("old")),
            sha = "s"
          ))
        }
        args <- list(...)
        expect_identical(args$sha, "s")
        list()
      },
      .package = "gh"
    )
    changed <- copilot_put_file("o", "r", "p", "new", "b", "m")
    expect_true(changed)
    expect_true(any(startsWith(calls, "PUT")))
  })
})

describe("copilot_sync_repo", {
  fake_skills <- list(
    brand = "b",
    blog = "bl",
    social = "s",
    translation = "t",
    brand_context = "ctx"
  )

  it("opens a PR when files changed", {
    local_mocked_bindings(
      copilot_gather_skills = function(config) fake_skills,
      gh_branch_upsert = function(...) "sha",
      copilot_put_file = function(...) TRUE,
      gh_open_or_update_pr = function(...) "https://github.com/rladies/x/pull/1"
    )
    result <- copilot_sync_repo("rladies", "x")
    expect_identical(result$status, "created")
    expect_identical(result$url, "https://github.com/rladies/x/pull/1")
  })

  it("reports unchanged and opens no PR when nothing changed", {
    opened <- FALSE
    local_mocked_bindings(
      copilot_gather_skills = function(config) fake_skills,
      gh_branch_upsert = function(...) "sha",
      copilot_put_file = function(...) FALSE,
      gh_open_or_update_pr = function(...) {
        opened <<- TRUE
        "url"
      }
    )
    result <- copilot_sync_repo("rladies", "x")
    expect_identical(result$status, "unchanged")
    expect_null(result$url)
    expect_false(opened)
  })
})
